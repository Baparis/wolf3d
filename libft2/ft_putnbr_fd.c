#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	u;

	u = n;
	if (u < 0)
	{
		ft_putchar_fd('-', fd);
		u = -u;
	}
	if (u > 9)
		ft_putnbr_fd(u / 10, fd);
	ft_putchar_fd((u % 10) + '0', fd);
}
